public class ifelseladder{
    public static void main(String[]args){
        int mark=75;
        if(mark>=90){
            System.out.println("A Grade");
        }
        else if(mark>=80){
            System.out.println("B Grade");
        }
        else if(mark>=60){
            System.out.println("c Grade");
        }
        else if(mark>=40){
            System.out.println("d Grade");
        }
        else
        {
            System.out.println("Fail");
        }
    }

        }